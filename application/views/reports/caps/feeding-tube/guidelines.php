                            Лечебная бригада должна приложить все усилия к тому, чтобы лицо, ответственное за принятие решений в интересах
                            пациента, приняло решение на основании всей полноты информации. В исключительных случаях рекомендуется обращаться
                            в этическую комиссию или в суд.
