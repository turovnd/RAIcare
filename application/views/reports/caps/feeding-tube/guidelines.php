                        <li>
                            Проверка кожи вокруг места установки питающей трубки на предмет появления признаков кровотечения или
                            местной инфекции (например, покраснений, отеков или гнойных выделений).
                        </li>
                        <li>
                            Отслеживание признаков или симптомов непроходимости желудочно-кишечного тракта (вздутие живота, боль,
