                    Подумайте над тем, какими иными способами можно поддерживать социализацию пациента с питающей трубкой.
                </li>
                <li>
                    Обеспечьте эмоциональную и социальную поддержку пациента со стороны социальных работников,
                    духовенства и других членов лечащей бригады.
                </li>
            </ul>

