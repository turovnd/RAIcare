                        </li>
                        <li>
                            Подумайте о том, каким членам лечащей бригады, включая терапевта, можно поручить обсудить с пациентом
                            пересмотр его статуса, представить ему риски и преимущества кормления через трубку по сравнению с паллиативной
                            терапией или ручным кормлением, и выяснить согласие / несогласие пациента с кормлением через трубку. Решение
                            должно быть принято <b>на основании должного объема информации.</b>
                        </li>
                        <li>
                            Если есть необходимость, привлеките к участию в процессе принятия решения члена (членов) семьи пациента.
                        </li>
                        <li>
                            Уважайте выбор пациента.
                        </li>
                    </ul>
                    <p class="text-bold">
                        Пациент утратил когнитивные способности и не может принимать решения, касающиеся его здоровья?
