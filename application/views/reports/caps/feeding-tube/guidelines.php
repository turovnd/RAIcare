                <div class="m-b-20">
                    <b>Применены ли к пациенту ограничители физических движений? Получает ли он психотропные средства?</b>
                    У пациентов, которых кормят через трубку, часто имеются когнитивные нарушения и такие пациенты легче возбуждаются.
                    Они могут попытаться вытащить питающую трубку. Поэтому возбужденные пациенты с установленными питающими трубками
                    подвергаются значительному риску применения ограничителей физических движений или введения психотропных средств для
                    успокоения. Исследования показали, что пациенты, проживающие в учреждениях по долговременному уходу и которых кормят
                    через трубку, с большей вероятностью столкнутся с применением ограничителей физических движений по сравнению с пациентами,
                    которых не кормят через трубку. <b>Применение как ограничителей физических движений, так и психотропных средств может иметь
                    серьезные неблагоприятные последствия; их применения следует избегать и обращаться к ним только в качестве последней
                    возможности для удержания трубки на месте.</b>
                </div>

                <div class="m-b-20">
                    <b>Периодически оценивайте оправданность продолжения кормления через трубку.</b> Решение вставить питающую трубку было
                    принято в более ранний период времени. Как и при всех остальных способах медицинского вмешательства, при длительном
                    использования питающей трубки необходимо периодически оценивать ход этого процесса, чтобы быть уверенным в том, что
                    использование трубки по-прежнему соответствует всем целям ухода за пациентом. С технической точки зрения, прекратить
                    кормление через трубку очень просто (большинство гастроскопических трубок разработаны таким образом, чтобы их удаление
                    осуществлялось с умеренным трением – безопасно и безболезненно). Однако с эмоциональной и этической точек зрения,
                    прекращение кормления через трубку может быть очень трудным, и необходимо поддержать все психосоциальные потребности
                    пациента и семьи.
                </div>

                <div class="m-b-20">
                    <p class="text-bold">Возможные причины для прекращения питания через трубку:</p>
                    <ul>
                        <li>
                            Состояние пациента улучшилось до такой степени, что он может есть и пить через рот. Решите, не следует ли привлечь
                            к участию в соответствующей оценке пациента специалиста в области питания, логопеда-психолога или эрготерапевта.
                        </li>
                        <li>
                            Общее состояние пациента не улучшилось, добиться адекватного приема пищи через рот не удалось, и кормление через
                            трубку более не отвечает целям ухода. Следует рассмотреть альтернативные стратегии по управлению состоянием пациента,
                            в том числе:
                            <ul class="m-b-0">
                                <li>
                                    Добросовестный подход к ручному кормлению пациента. Хотя ручное кормление пациента может не повлиять на
                                    показатели приема калорий, оно может несколько улучшить качество жизни пациента.
                                </li>
                                <li>
                                    Поддерживающий уход (например, болеутоление, меры против одышки, использование тампонов, смоченных глицерином,
                                    или ледяных кубиков для поддержания рта смоченным, уход за кожей, психологическая поддержка и т.д.) за пациентом
                                    с заболеванием в терминальной стадии.
                                </li>
                            </ul>
                        </li>
                    </ul>
                </div>

                <div class="m-b-20">
                    <p class="text-bold">
