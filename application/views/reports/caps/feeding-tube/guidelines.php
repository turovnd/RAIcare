                        </li>
                        <li>
                            Организуйте встречу с уполномоченными членами лечащей бригады, включая терапевта, и с лицом,
                            уполномоченным на принятие решений от имени пациента, по вопросу изменения статуса пациента,
                            обсуждения рисков и преимуществ кормления через трубку по сравнению с паллиативной терапией,
                            и обмена мнениями относительно продолжения кормления пациента через трубку.
                        </li>
                    </ul>
                    <p class="text-bold">
                        Если пациент утратил когнитивные способности и не может принимать решения, касающиеся его здоровья,
                        получило ли лицо, ответственное за принятие решений в интересах пациента, совет подумать над перечисленными ниже вопросами?
                    </p>
                    <ul>
                        <li>
                            Оставил ли пациент заблаговременное письменное распоряжение относительно того, желает ли он,
                            чтобы его кормили через трубку при текущей ситуации?
                        </li>
                        <li>
                            Сообщал ли пациент свою волю относительно того, желает ли он, чтобы его кормили через трубку при
                            текущей ситуации, устно членам семьи или сотрудникам лечебного учреждения?
                        </li>
                        <li>
                            Оценив все, что стало известно о ценностях и предпочтениях пациента, считает ли лицо, ответственное
                            за принятие решений в интересах пациента, что пациент пожелал бы продолжать кормление через трубку
                            при текущей ситуации, если бы был в состоянии принимать решения (решение представителя)?
                        </li>
                        <li>
                            С учетом информации о рисках и преимуществах кормления через трубку отвечает ли интересам пациента
                            продолжение кормления через трубку?
                        </li>
                    </ul>
                    <p class="text-bold">
                        Нет ли конфликта между заблаговременным распоряжением пациента или его интересами, с одной стороны, и
                        выбором лица, ответственного за принятие решений в интересах пациента, с другой?
                    </p>
                    <ul>
                        <li>
                            В большинстве случаев, если выбор лица, ответственного за принятие решений в интересах пациента, делается
                            им на основе полной информации, он совпадает с пожеланиями пациента, выраженными в заблаговременном
                            распоряжении. В этой ситуации следует уважать выбор лица, ответственного за принятие решений в интересах пациента.
                        </li>
                        <li>
                            Однако в более редких случаях выбор лица, ответственного за принятие решений в интересах пациента, как
