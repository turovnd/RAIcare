    <h3 class="h3">Указания по реализации ПКО кормления через питающую трубку</h3>

    <div class="p-l-30">

        <div class="m-b-20">
            <p class="text-bold">Участие лечащей бригады в оценке пациентов с питающей трубкой и работе с ними </p>
            <p>
                <b>Использование питающих трубок</b> – это комплексная проблема. Для того чтобы правильно оценивать состояние пациентов с
                питающими трубками и правильно ухаживать за такими пациентами, необходимы знания во многих областях. Главную роль играют
                медсестры, и они должны решать, когда обратиться за советом к другим членам бригады. Медсестры должны быть готовы уведомлять
                терапевта и диетолога о проблемах и осложнениях, возникающих при уходе, а также об опасениях относительно уместности кормления
                через питающую трубку. Рекомендуется, чтобы специалист в области питания или диетолог регулярно следили за состоянием всех
                пациентов, получающих пищу через питающую трубку, чтобы убедиться в том, что осуществляемый прием пищи отвечает потребностям
                пациента. К числу других специалистов, участие которых может потребоваться, относятся психологи-логопеды (для оценки
                способности к еде и проглатыванию пищи), эрготерапевты (для поддержания правильной осанки пациента во избежание аспирации),
                социальные работники и представители духовенства (для разрешения этических вопросов и психологической поддержки).
            </p>
            <p>
                <b>Были ли учтены психологические соображения?</b> Прием пищи – это социальное мероприятие, от которого можно получать
                удовольствие, а кормление другого человека – это проявление заботы о нем. Потеря способности самостоятельно принимать
                пищу может привести к неблагоприятным психологическим последствиям как для пациента, так и для членов его семьи.
            </p>
            <ul>
                <li>
                    Отслеживайте возможные признаки депрессии.
