        <div class="m-b-20">
            <h4 class="h4">Подход к пациентам с проблемами в области проведения досуга</h4>

            <h4 class="h4 text-uppercase">Первоначальная оценка</h4>

            <div class="p-l-30">
                <p>
                    Есть ли у пациента индивидуальные предпочтения в области досуга? Для того
                    чтобы ответить на этот вопрос, необходимо поговорить с пациентом и/или членом семьи или другом, который
                    хорошо знает пациента (обратитесь к ним во время посещения – и учитывайте, что 80% таких пациентов
                    поддерживают тесные связи с семьей).
                </p>
                <p>
                    В каких мероприятиях в области досуга предпочитал участвовать пациент до присоединения к программе?
                </p>
                <ul>
                    <li>
                        Это были пассивные или активные мероприятия? (Например, проводил ли пациент значительное время
                        перед телевизором или за чтением книг?)
                    </li>
                    <li>
                        Участвовал ли он в мероприятиях, будь то с участием членов семьи или без них, проводимых вне дома
                        (например, посещал церковные службы, клубы спортивные мероприятия, путешествовал, ходил в ресторан)?
                    </li>
                    <li>
                        Проводились ли они, в основном, при поддержке семьи — супруга (супруги), детей, внуков?
                    </li>
                    <li>
                        Проводились ли они, в основном, при поддержке внесемейных/местных сообществ?
                    </li>
                </ul>
                <p>
                    Какими видами деятельности пациент занимается в настоящее время?
                </p>
                <ul>
                    <li>
                        Составьте полный список досуговых мероприятий, включая те занятия, в которых пациент принимает участие
                        самостоятельно и/или по собственной инициативе, и те, в которых пациент принимает участие совместно с
                        другими людьми и/или которые организованы другими.
                    </li>
                    <li>
                        Приходят ли к пациенту посетители и если да, то какими видами деятельности они совместно занимаются?
                    </li>
                    <li>
                        В каких заранее планируемых мероприятиях участвует пациент на момент проведения оценки?
                    </li>
                </ul>
                <p>
                    Меняется ли режим участия в мероприятиях согласно сезонным изменениям в программе или эти изменения
                    влияют на степень вовлеченности пациента в досуговые мероприятия?
                </p>
                <ul>
                    <li>
                        Какими другими видами деятельности было бы интересно позаниматься пациенту? Включите в этот список те
                        виды деятельности, которые в настоящий момент недоступны или не предлагаются пациенту.
                    </li>
                    <li>
                        Какие уникальные навыки или знания есть у пациента, которые он мог бы передать другим людям? Умеет
                        ли он играть в карточные игры или в шахматы? Может ли он помочь окружающим в заполнении сложных форм
                        (например, налоговых форм)? Есть ли у него знания по теме, которая интересна другим людям (например,
                        знания о бейсболе)?
                    </li>
                </ul>
                <p>
                    Определение природы проблем, которые вызывают снижение вовлеченности пациента
                </p>
                <ul>
                    <li>
                        Определяя потребность пациента в досуге или возможные меры вмешательства в рамках планирования ухода
                        за пациентом, важно определить, почему пациент принимает решение участвовать либо не участвовать в
                        досуговых мероприятиях.
                    </li>
                    <li>
                        Пациент отказывается от участия в этих мероприятиях из-за функциональных или когнитивных проблем?
                        Оцените выносливость, мобильность пациента, способность удерживать равновесие, способность выражать
                        свои мысли, способность понимать окружающих и способность принимать решения.
                    </li>
                    <li>
                        Рассмотрите индикаторы психосоциального благополучия, например, инициативность и вовлеченность в
                        социальную жизнь.
                    </li>
                    <li>
                        Пациента кто-то заставил почувствовать себя нежеланным гостем? Устанавливают ли люди, уже участвующие
                        в досуговом мероприятии, какие-то барьеры, которые пациенту трудно перешагнуть?
                    </li>
                    <li>
                        Представляет ли состояние здоровья пациента барьер такого рода? Не страдает ли пациент недержанием,
                        не мучают ли его боли?
                    </li>
                    <li>
                        Может быть, пациенту мешает чувство неловкости или беспокойства из-за наличия на нем медицинского
                        оборудования (например, трубок, кислородного баллона, стомы) или нахождения в инвалидном кресле?
                    </li>
                    <li>
                        Возможно, пациенту не хватает каких-то ресурсов для участия в мероприятии?
                    </li>
                    <li>
                        Есть ли у пациента возможности познакомиться с другими пациентами, живущими в учреждении (например,
                        совместный прием пищи, чаепития, празднование дней рождения, группы воспоминаний)?
                    </li>
                    <li>
                        Снижают ли культурные ожидания пациента его интерес к предлагаемым ему занятиям?
                    </li>
                    <li>
                        Всегда ли у пациента были трудности с присоединением к новым компаниям? Если да, то принесет ли
                        успех инициатива самой компании по отношению к пациенту (и были ли такие случаи), или же пациент
                        просто предпочитает более пассивный или самодостаточный досуг?
                    </li>
                </ul>
                <p>
                    Является ли проблемой недоступность определенных видов досуга?
                </p>
                <ul>
                    <li>
                        Учреждение, в котором проживает пациент, может не иметь организованной программы в тех областях,
                        которые интересны пациенту – в области игр, упражнений, религиозных бесед.
                    </li>
                    <li>
                        Есть ли возможность снизить барьеры для участия в соответствующей деятельности, тем самым упростив
                        доступ пациента к этой деятельности?
                    </li>
                    <li>
                        Располагает ли пациент достаточным количеством свободного времени для независимого и самостоятельного
                        проведения досуга?
                    </li>
                </ul>
                <p>
                    Если ли проблемы в области среды или персонала, которые затрудняют участие пациента в досуговых мероприятиях?
                </p>
                <ul>
                    <li>
                        Есть ли физические барьеры, перекрывающие пациенту доступ к пространству, где проводятся мероприятия?
                    </li>
                    <li>
                        Достаточно ли у учреждения специально выделенного персонала для организации социальных досуговых мероприятий?
                    </li>
                    <li>
                        Возможно, персоналу не хватает времени на привлечение других пациентов к уже имеющимся досуговым программам?
                    </li>
                    <li>
                        Не забывают ли представители персонала о хрупкости душевной организации пациента, не вызывают ли у него
                        чувство страха?
                    </li>
                </ul>
            </div>
        </div>

