            <h4 class="h4 text-uppercase">
                Лечение и мониторинг
            </h4>
            <div class="p-l-30">
                <p>
                    План по борьбе с неактивным поведением пациента и по привлечению его к досуговым мероприятиям должен
                    строиться с опорой на сильные стороны и предпочтения пациента. Если мероприятия требуют от пациента
                    слишком больших усилий, это может отвратить пациента со значительными нарушениями от участия в таких
                    мероприятиях, а если мероприятия, напротив, предполагают слишком простые цели, это может оказаться
                    недостаточной мотивацией для пациента. Программу досуговых мероприятий можно использовать в
                    разнообразных целях, в том числе в целях реабилитации или восстановления или предотвращения
                    ослабления функций, или просто для повышения вовлеченности пациента в социальную жизнь. Важно не
                    только предлагать программы досуговых мероприятий, но и помнить, что образование как форма
                    проведения досуга – это одна из составляющих общей стратегии снижения пассивности пациентов.
                    Для некоторых пациентов важно, чтобы в программу терапии включались образовательные элементы и
                    консультации о том, как организовать досуг пациента – как в домашних условиях, так и в учреждении
                    по долговременному уходу. Вмешательства, которые требуют участия членов семьи пациента в досуговых
                    мероприятиях, могут оказаться особенно эффективными.
                </p>
                <ul>
                    <li>
                        В качестве первого шага к повышению досуговой активности пациента могут быть использованы
                        пассивные и самостоятельные формы проведения досуга. Около 90% пациентов, которым требуется
                        повышение уровня активности, не испытывают неудобств, если досуговая деятельность проходит в
                        его комнате. К числу занятий, которые представляют интерес для большинства из пациентов,
                        которым показано вмешательство, относится просмотр телепередач, прослушивание музыки и беседы
                        с другим человеком один-на-один. В несколько меньшей степени, около 40%, пациенты интересуются
                        чтением.
                    </li>
                    <li>
                        Хотя в группе пациентов, которым показано вмешательство, имеются культурные различия,
                        значительная доля пациентов выразила предпочтение в отношении следующих коллективных
                        интерактивных занятий: занятия, связанные с духовными аспектами жизни (50%), игра в
                        карты (40%) и занятия ремеслом (25%).
                    </li>
                    <li>
                        Среди занятий, связанных с физической активностью, наибольший интерес представляют физические
                        упражнения (около 45%) и уличные прогулки (50%).
                    </li>
                    <li>
                        Составляя программы досуговых мероприятий, опирайтесь на сильные стороны и предпочтения пациента.
                        <ul class="m-b-0">
                            <li>
                                Выбирая из списка доступных мероприятий, убедитесь в том, что эти мероприятия будут
                                уместны с учетом возраста и культурных особенностей пациента.
                            </li>
                            <li>
                                Будет ли полезно пациенту расширять его досуговые мероприятия так, чтобы в них могли
                                принимать участие и другие люди со сходными интересами?
                            </li>
                            <li>
                                Не выбирайте мероприятия, если участие в таких мероприятиях будет смущать пациента,
                                оскорблять его достоинство или ставить его в неудобное положение. Имейте в виду, что
                                такие реакции связаны как с личностными особенностями пациента, так и с
                                социокультурными факторами.
                            </li>
                            <li>
                                Ищите такие виды досуговой деятельности, которые будут интересными и в меру сложными
                                для пациента, но не будут явно выходить за пределы его когнитивных или психологических
                                возможностей.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Будьте внимательны к признакам психиатрических нарушений. Если показатель пациента по Шкале
                        депрессии (DRS) составляет 3 балла или выше, проинформируйте об этом специалиста в области
                        душевного здоровья. Это может быть признаком депрессии и потребует соответствующего вмешательства.
                    </li>
                    <li>
                        Такие симптомы, как отказ от коллективных мероприятий и сниженный уровень социального
                        взаимодействия, могут быть следствием проблем с душевным здоровьем, например, депрессии
                        или шизофрении.
                    </li>
                    <li>
                        Глубокий конфликт с другими пациентами, семьей или персоналом может стать серьезным барьером
                        для участия в программе мероприятий и получении удовольствия от такого участия. Выясните, как
                        можно помочь разрешению конфликта, если он принимает затянувшиеся формы.
                    </li>
                    <li>
                        Придумайте, как привлечь к разрешению конфликта семью, друзей, местные группы и волонтеров.
                        Обсудите с семьей пациента схему и характер посещений.
                    </li>
                </ul>
            </div>
