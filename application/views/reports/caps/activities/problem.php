    <h3 class="h3">Описание проблемы</h3>
    <div class="p-l-30">
        <p>
            ПКО досуга определяет пациентов, у которых есть некоторый нереализованный запас когнитивных способностей,
            но которые либо устранились от участия в досуговых мероприятиях, либо их непросто вовлечь в ту или иную
            деятельность и в социальные отношения. Одна из задач ухода – разработка стратегий, которые помогут этим
            пациентам повысить свою активность в досуговых мероприятиях разного рода (например, игре в карты, чтении
            книг, воспоминаниях или просмотре фильмов).
        </p>
        <p>
            Активный образ жизни, особенно если он согласуется с текущими функциональными возможностями пациента и
            кругом занятий, который пациент вел в прошлом, может сыграть ключевую роль в поддержании позитивного
            мироощущения и общей самооценки и ощущения благополучия. Около двух третей пациентов, которым показано
            вмешательство в соответствии с данным ПКО, не обладают позитивным мироощущением и не видят смысла в
            своей повседневной жизни.
        </p>
        <p>
            При таком положении очень важно установить контакт с такими пациентами. Также мы должны признать, что
            их общие функциональные и когнитивные характеристики не отличаются сколько-нибудь значительно от
            функциональных и когнитивных характерстик других пациентов, проживающих в аналогичных условиях.
            Таким образом, ключевая задача –  заниматься не другими осложнениями, а сразу перейти к попыткам
            лучше вовлечь пациента в круг разнообразных досуговых мероприятий. В то же время предлагаемые
            программы по расширению круга деятельности пациента должны учитывать когнитивные, физические и
            социальные возможности пациента.
        </p>
        <p>
            Важно убедиться в том, что при составлении плана учтено, какой деятельностью предпочитает заниматься
            пациент, – вне зависимости от того, соответствует ли эта деятельность активному или пассивному образу
            жизни. Важно, чтобы пациенту этим планом не навязывались пристрастия его составителя. Деятельность, в
            которой предлагается участвовать пациенту, должна быть сосредоточена на том, чтобы пациент реализовал
            себя и свои желания, использовал свои физические или когнитивные возможности, получал удовольствие и
            установил канал связи с окружающими.
        </p>
    </div>
