                    </li>
                    <li>
                        <b>Пересмотрите план ухода и включите в него дополнительный присмотр или программу наблюдения, которые
                            помогут пациенту восстановить прежний уровень активности. </b>
                        Не забывайте о необходимости восстановить уверенность пациента в себе, произвести необходимые изменения во
                        избежание возможных проблемы, которые могут быть возникнуть в связи с падением, а также о необходимости провести
                        адаптационные мероприятия, связанные с травмами, полученными пациентом в результате падения.
                    </li>
                </ul>

                <p>
                    Если не удалось обнаружить ни одного из перечисленных выше нарушений состояния здоровья, продолжайте разбираться в
