            <h4 class="h4 text-uppercase">
                Предложения по планированию ухода после падения
            </h4>
            <div class="p-l-30">
                <ul>
                    <li class="text-bold">
                        Рассмотрите вопрос о замене назначенных лекарственных средств, времени их принятия или дозировок;
                    </li>
                    <li class="text-bold">
                        Рассмотрите вопрос об обращении к специалистам по лечебной физкультуре или эрготерапии за консультацией, которая
