                        назначений до появления соответствующих симптомов.
                    </li>
                    <li>
                        Обсудите результаты всех лабораторных анализов с терапевтом.
                    </li>
                    <li>
                        Попросите пациента описать, как произошло падение.
                    </li>
                    <li>
                        Оцените потребность пациента во вмешательстве, призванном избежать потери функциональности,
                        которая может произойти вследствие падения. Травмы, низкая активность по причине болезни или
                        опасение упасть снова – все эти факторы способствуют снижению функциональности пациента.
                    </li>
