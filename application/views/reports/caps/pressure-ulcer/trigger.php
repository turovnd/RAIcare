                    усугубится до II или более высокой стадии, приблизительно у 15%, если они они проживают в учреждениях по
                    долговременному уходу, и у 7%, если они получают уход на дому. В то же время у гораздо большего числа
                    пациентов, страдающих от пролежневых язв I степени, по истечении 90 дней язвы исчезнут: приблизительно
                    у 67% пациентов, проживающих в учреждениях по долговременному уходу, и у 45% пациентов, если они
