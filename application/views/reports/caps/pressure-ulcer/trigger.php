            </h4>
            <div class="p-l-30">
                <p>
                    При любой схеме медицинского обслуживания доля пациентов с пролежневыми язвами такой степени отчасти будет зависеть
                    от бдительности персонала / лиц, осуществляющих уход за пациентом, в области предотвращения появления пролежневых язв.
                </p>
                <p>
                    Диапазон распространенности тяжелых пролежневых язв в учреждениях по долговременному уходу может быть широким: в
                    некоторых учреждениях может быть немного пациентов, страдающих от пролежневых язв II или более высокой степени, а
                    в других учреждениях таких пациентов может быть, напротив, много. В эту категорию попадают, в среднем, около 10%
                    пациентов лечебных учреждений по долговременному уходу, включая тех, кто поступил туда уже с пролежневой язвой.
