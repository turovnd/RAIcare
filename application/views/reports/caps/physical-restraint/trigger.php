            <h4 class="text-uppercase h4">
                ВМЕШАТЕЛЬСТВО ПОКАЗАНО В ЦЕЛЯХ ОТКАЗА ОТ ОГРАНИЧИТЕЛЕЙ – ПАЦИЕНТАМ НЕСПОСОБНЫМ ИЛИ ПОЧТИ НЕСПОСОБНЫМ ВЫПОЛНЯТЬ
                ПОВСЕДНЕВНЫЕ ДЕЙСТВИЯ, СООТВЕТСТВУЮЩИЕ РАННЕМУ ИЛИ СРЕДНЕМУ ЭТАПУ ПОТЕРИ ФУНКЦИОНАЛЬНОСТИ
            </h4>
            <div class="p-l-30">
                <p>
                    У пациентов, относящихся к этой подгруппе, вероятность наличия истории падений и проблем с поведением выше, чем
                    в подгруппе пациентов, которым не показано вмешательство в соответствии с данным ПКО. Примерно 70% этих пациентов
                    страдают от сильной потери когнитивных функций и неспособны ходить или использовать инвалидное кресло. Около 40%
                    не в состоянии самостоятельно принять вертикальное положение сидя, более чем у четверти имеются серьезные проблемы
                    со зрительным распознаванием и пониманием окружающих, а 15% из них кормят через пищевую трубку. При наличии
                    эффективных программ по сокращению использования ограничителей движений лишь немногие пациенты будут по-прежнему
                    нуждаться в ограничителях (или таких пациентов не будет совсем).
                </p>
                <p>
                    В эту группу входит от 1 до 6% пациентов, находящихся в учреждениях по долговременному уходу, и менее 1% пациентов,
                    получающих уход на дому.
                </p>
