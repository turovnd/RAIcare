    <h3 class="h3">Схема определения показаний ПКО ограничителей движений</h3>

    <div class="p-l-30">

        <div class="m-b-20">
            <p>
                Этот ПКО применяется к пациентам, находящимся в учреждениях по долговременному уходу или по реабилитации
                после неотложной помощи. Этот ПКО не применяется к пациентам, получающим уход на дому или находящихся в
                иных жилищных условиях вне лечебных учреждений.
            </p>
            <p>
                ПКО ограничителей движений определяет два вида пациентов, которым показано последующее врачебное наблюдение,
                на основе Иерархической шкалы эффективности повседневной деятельности (ADL Self-Performance Hierarchy Scale).
                На момент проведения оценки обе группы должны быть ограничены в движениях такими приспособлениями, как
                ограничитель туловища или стулья, препятствующие вставанию. Группы различаются уровнем эффективности
                повседневной деятельности: Одна группа лучше вовлечена в повседневную деятельность, а другая демонстрирует
                меньшую независимость в этих действиях.
            </p>
        </div>


        <div class="m-b-20">
            <h4 class="text-uppercase h4">
                ВМЕШАТЕЛЬСТВО ПОКАЗАНО В ЦЕЛЯХ ОТКАЗА ОТ ОГРАНИЧИТЕЛЕЙ – ПАЦИЕНТАМ НА РАННЕМ ИЛИ СРЕДНЕМ ЭТАПЕ ПОТЕРИ
                ФУНКЦИОНАЛЬНОСТИ, СПОСОБНЫМ ВЫПОЛНЯТЬ НЕКОТОРЫЕ ПОВСЕДНЕВНЫЕ ДЕЙСТВИЯ (например, личная гигиена, одевание и ходьба)
            </h4>
            <div class="p-l-30">
                <p>
                    Движения пациентов, относящихся к этой подгруппе, ограничивают обычно из-за того, что персонал опасается падений,
                    хочет пресечь бесцельное брожение и проблемы с поведением (например, сопротивление оказанию медицинской помощи,
                    физическая агрессия или социально неприемлемое поведение). В эту группу попадет приблизительно одна пятая часть
                    всех пациентов, подвергающихся ограничению движений. Организации с эффективными программами по сокращению случаев
                    ограничения движений добиваются исключения таких случаев в отношении пациентов указанной группы.
                </p>
                <p>
                    В эту группу входит 1% пациентов учреждений по долговременному уходу и 2% пациентов, получающих уход на дому.
                </p>
            </div>
        </div>


