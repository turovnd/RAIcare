    <h3 class="h3">Описание проблемы</h3>
    <div class="p-l-30">
        <p>
            ПКО кардиореспираторных нарушений напоминает специалистам в области охраны здоровья о необходимости оценивать резидента на
            предмет возможного наличия сердечно-сосудистых или респираторных заболеваний, а в случае их обнаружения – лечить их.
            Многие, хотя и не все, резиденты, страдающие от кардиореспираторных проблем, скорее всего, уже находятся под наблюдением
            терапевта. Кроме того, у некоторых резидентов могут появиться новые симптомы или обостриться старые, что может потребовать
            медицинского или иного вмешательства.
        </p>
        <p>
            Заболеваемость сердечными заболеваниями быстро повышается по мере старения резидента. В западных странах 75% всех резидентов
            с сердечной недостаточностью – это люди старше 60, а у 20% резидентов в возрасте 75 лет и старше в анамнезе присутствует
            сердечный приступ или стенокардия. Соответствующие симптомы могут развиться у многих пожилых резидентов с гипертонической
            болезнью при резкой смене артериального давления или при приеме нового лекарственного средства. Широко распространена
            хроническая обструктивная легочная болезнь (COPD), особенно у курящих резидентов и у резидентов, работающих на некоторых
            предприятиях с вредными условиями.
        </p>
        <p>
            Некоторые признаки и симптомы, например, одышка или боль в груди при физической нагрузке, могут явно указывать на наличие
            проблем в кардиореспираторных системах. Однако в некоторых случаях, например, при общей усталости, обусловленность симптомов
            проблемами в кардиореспираторных системах может быть не столь очевидна. Более того, при наличии у резидента некоторых хронических
            заболеваний, например, хронической обструктивной легочной болезни, признаки и симптомы острого заболевания, например,
            пневмонии, может быть трудно распознать. Все эти проблемы могут существенно повлиять на образ жизни резидента, и эти проблемы
            следует отслеживать и принимать меры по их снятию.
        </p>
