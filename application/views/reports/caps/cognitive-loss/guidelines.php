    <div class="p-l-30">

        <div class="m-b-20">

            <h4 class="h4 text-uppercase">
                Первоначальное диагностическое обследование в случае, когда диагноз «деменция» не поставлен
            </h4>

            <div class="p-l-30">
                <p>
                    Даже если балл по Шкале эффективности когнитивной деятельности низок, резидент уже может находиться
                    на начальных стадиях деменции. Если есть подозрение на нарушение когнитивной деятельности,
                    рекомендуется провести дальнейшую оценку и назначить консультации у специалистов. В тех случаях,
                    когда резидент и члены семьи предоставляют неопределенную информацию, может потребоваться
                    непосредственная консультация терапевта.
                </p>
                <p>
                    Следует рассмотреть вопрос о направлении на дальнейшее обследование у терапевта тех резидентов,
                    которым не поставлен диагноз «деменция», в случае, если
                </p>
                <ul>
                    <li>
                        их балл по Шкале эффективности когнитивной деятельности 1, 2 или 0, но присутствуют другие
                        свидетельства ослабления когнитивной деятельности (например, ослабление когнитивных
                        способностей за последнее время). В таком случае резиденты, ослабление когнитивных функций
                        которых пока не имеет объяснения, должны пройти полную медицинскую оценку (evaluation),
                        которая позволит исключить (или подтвердить) причины ослабления когнитивных функций,
                        подлежащие лечению, и определить оптимальные способы медицинского вмешательства.
                    </li>
                    <li>
                        другие признаки ослабления когнитивных функций – это симптомы, перечисленные под рубрикой
                        «факторы клинического риска» в разделах, где определены группы резидентов и показанное им
                        вмешательство. Кроме того, таким признаком может быть снижение эффективности инструментальной
                        и базовой повседневной деятельности, которое не объясняется физическим заболеванием или
                        нарушением состояния здоровья.
                    </li>
                </ul>
                <p>
                    Если необходимость во внешней помощи все еще неясна, обратитесь к терапевту. Изучите медицинские
                    карты или историю болезни резидента и самые свежие оценки interRAI, чтобы выявить иные возможные
                    причины (помимо ослабления когнитивных способностей), которые ведут к этой потребности во внешней
                    помощи при осуществлении повседневной деятельности. К числу таких причин могут относиться:
                </p>
                <ul>
                    <li>
                        Физиологическое расстройства, например:
                        <ul class="m-b-0">
                            <li>
                                Болезни костно-мышечной системы
                            </li>
                            <li>
                                Неврологические заболевания
                            </li>
                            <li>
                                Посттравматические нарушения
                            </li>
                        </ul>
                    </li>
                    <li>
                        Факторы, связанные с образом жизни и приобретенными привычками, например:
                        <ul class="m-b-0">
                            <li>
                                Избыточное употребление алкоголя
                            </li>
                            <li>
                                Использование психотропных средств
                            </li>
                            <li>
                                Правила домохозяйства (например, у многих пар принято, что один из партнеров может
                                брать на себя ответственность за определенные функции, например, работу по дому
                                или управление домашними финансами).
                            </li>
                        </ul>
                    </li>
                </ul>
                <p>
                    Если необходимость во внешней помощи все еще неясна, обратитесь к терапевту. Даже если будет
                    найдено правдоподобное объяснение снижения эффективности, продолжайте отслеживать признаки
                    ослабления эффективности когнитивных или физической деятельности.
                </p>
                <p>
                    Однако всем резидентам, балл которых по Шкале эффективности когнитивной деятельности составляет
                    1 или 2, следует пройти полное медицинскую оценку, которая позволит исключить (или подтвердить)
                    причины ослабления когнитивных функций, подлежащие лечению, и определить оптимальные способы
                    медицинского вмешательства.
                </p>
                <p>
                    Диапазон действий, которые можно предпринять после такой оценки очень широк: от однократной
                    консультации у терапевта до полного процесса, включающего нейропсихологическое тестирование,
                    гематологические исследования и сканирование мозга.
                </p>

                <p class="text-bold">
                    Ищите те причины ослабления когнитивных функций, которые можно исправить
                </p>
                <p>
                    Почти любое новое лекарственное средство может оказать отрицательное воздействие на когнитивные функции.
                </p>
                <p>
                    Некоторые виды потери когнитивных функций, например, обусловленные низким уровнем витамина B12,
                    нарушениями метаболизма кальция или щитовидной железы, или избыточным употреблением алкогольных
                    напитков, обратимы. Изучите факторы клинического риска на предмет возможной потери когнитивных
                    функций в будущем у тех резидентов, которым показано вмешательство с целью предотвращения
                    ослабления когнитивных функций.
                </p>

                <p class="text-bold">
                    Признаки нестабильного состояния когнитивных функций
                </p>
                <p>
                    Бред или острая спутанность сознания – это потенциально обратимые состояния. Однако эти состояния
                    могут дать ложное представление о том, что данные когнитивные ограничения резидента типичны или
                    обычны для него. После окончания бредового состояния у многих резидентов можно ожидать повышения
                    эффективности когнитивной деятельности.
                </p>

                <p class="text-bold">
                    Оценивайте признаки снижающейся эффективности когнитивной деятельности у резидентов, которым
                    поставлен диагноз «деменция» (любая)
                </p>
                <ul>
                    <li>
                        Лиц, осуществляющих уход за резидентами, следует попросить назвать потенциально обратимые
                        причины (помимо заболевания, непосредственно вызывающего деменцию) недавнего ослабления
                        когнитивных функций. Действия лиц, осуществляющих уход за резидентами, могут избыточно
                        компенсировать незначительную потерю функциональности. Цель ухода – дать резиденту возможность
                        максимально широко использовать свои сохраняющиеся когнитивные способности в повседневной
                        жизни. Было бы неверно предположить, что потеря когнитивных функций, произошедшая недавно,
                        необратима.
                    </li>
                    <li>
                        Выявление этих изменений может дать лицу, осуществляющему уход за резидентом, новую информацию
                        о когнитивных и функциональных ограничениях резидента, что позволит снова включить резидента в
                        принятие решений и осуществление повседневной деятельности. Если окружающие будут знать о том,
                        что у резидента ослабевают когнитивные функции, они смогут сформировать практически
                        ориентированные ожидания относительно возможностей резидента и способствовать максимальному
                        вовлечению резидента в принятие каждодневных решений.
                    </li>
                </ul>

                <p class="text-bold">
                    Наблюдаются ли поведенческие симптомы (например, бесцельное блуждание или физическая агрессия
                    по отношению к окружающим), которые мешают повседневной жизни, осуществлению ухода или участию
                    резидента в мероприятиях?
                </p>
                <ul>
                    <li>
                        Эти нарушения могут эффективно исправляться, а состояние когнитивных способностей – улучшаться
                        при применении специальных способов лечения поведенческих проблем и бредового состояния.
                    </li>
                    <li>
                        Болевые ощущения оказывают негативное воздействие на эффективность когнитивной деятельности.
                        Боль у резидентов с ослабленными когнитивными функциями также может вызвать проблемы с
                        поведением, раздражительность и потерю функций.
                    </li>
                    <li>
                        Отдельные поведенческие проблемы необратимы. Если это действительно так и если такое
                        поведение повторяется ежедневно, возможно, резидента следует поместить в другие условия.
                        Однако на ранних стадиях ослабления когнитивных функций резидент должен самостоятельно
                        принимать решения, касающиеся своей жизни, в том числе решать, где ему жить.
                    </li>
                    <li>
                        Некоторые поведенческие проблемы не представляют угрозу жизни или здоровью резидента,
                        образу жизни или безопасности окружающих и не требуют вмешательства. Но если между
                        поведением и ослаблением когнитивных функций присутствует временная связь, примите
                        решение о лечении соответствующих поведенческих проявлений. Следует дать ответ на
                        следующие вопросы:
                        <ul class="m-b-0">
                            <li>
                                Ослабли ли когнитивные функции после начала программы управления поведением
                                (например, после начала использования психотропных средств)?
                            </li>
                            <li>
                                Связано ли ослабление когнитивных функций с программой лечения (например,
                                с побочными эффектами какого-либо лекарства)?
                            </li>
                            <li>
                                Улучшилось ли состояние когнитивных функций после начала программы
                                управления поведением?
                            </li>
                            <li>
                                Повысила ли помощь лица, осуществляющего уход за резидентом, показатели
                                эффективности повседневной деятельности резидента?
                            </li>
                        </ul>
                    </li>
                </ul>

                <p class="text-bold">
                    Страдает ли резидент от симптомов депрессии?
                </p>
                <ul>
                    <li>
                        Симптомы депрессии могут вызвать ослабление когнитивных функций, которое часто ошибочно
                        принимается за деменцию.
                    </li>
                </ul>

                <p class="text-bold">
                    Есть ли  еще какие-либо медицинские проблемы?
                </p>
                <ul>
                    <li>
                        Выявление и излечение других медицинских проблем может улучшить состояние когнитивных функций
                        и качество жизни резидента. Например, терапевтическое лечение застойной сердечной недостаточности
                        или хронической обструкционной пневмонии может привести к ослаблению общих и когнитивных
                        функций, а такие нарушения состояния здоровья, как хронические болезни печени и почечная
                        недостаточность, могут спровоцировать или усугубить ослабление когнитивных функций. Эти
                        нарушения практически всегда находятся на контроле у терапевта. Поэтому, если резидент начинает
                        испытывать новое ослабление когнитивных функций, обсудите с терапевтом вероятность того, что
                        это ослабление связано с этими нарушениями или новым или измененным лечением этих нарушений.
                    </li>
                    <li>
                        Усиление боли может привести к снижению участия резидента в осуществлении функций, что может
                        быть ошибочно расценено как знак ослабления когнитивных функций.
                    </li>
                </ul>

                <p class="text-bold">
                    Может ли резидент эффективно общаться с окружающими?
                </p>
                <p>
                    Многие резиденты, страдающие от недостаточности когнитивных функций, неспособны к полноценному
                    осмысленному общению. Это приводит к тому, что поведение, которое на первый взгляд кажется
                    бессмысленным, может представлять собой единственно возможную форму общения резидента с окружающим
                    миром. Убедитесь в том, что неспособность эффективно общаться с окружающими не связана с проблемами
                    со слухом или неврологическими проблемами (например, афазией).
                </p>
                <ul>
                    <li>
                        Поговорите с ликом, осуществляющим уход за резидентом, и с семьей резидента о том, какие
                        способы общения с резидентом наиболее эффективны. Иногда именно эти люди лучше всех понимают,
                        как наладить контакт с резидентом; если это не так, то им потребуется помощь в разработке
                        нового подхода к резиденту, который в последнее время продемонстрировал снижение когнитивных
                        способностей.
                    </li>
                    <li>
                        Хочет ли резидент участвовать в осмысленном общении? Способен ли он к этому?
                    </li>
                    <li>
                        Пользуется ли лицо, осуществляющее уход за резидентом, невербальными техниками коммуникации
                        (например, прикосновениями, жестами) для того чтобы подтолкнуть резидента ответить?
                    </li>
                </ul>

                <p class="text-bold">
                    Изучите схему принимаемых лекарств, которые могли бы повлиять на когнитивные способности.
                </p>
                <p>
                    Возможно, вам следует побеседовать с терапевтом или обратиться к фармацевту-консультанту,
                    чтобы он помог найти ответы для этого обзора.
                </p>
                <ul>
                    <li>
                        Многие лекарства, в особенности психоактивные, могут вызвать ослабление когнитивных функций.
                    </li>
                    <li>
                        Из-за начала или окончания приема лекарственного средства когнитивные функции резидента могут
                        внезапно ослабнуть. Тщательный анализ схемы принимаемых лекарств (включая как рецептурные,
                        так и безрецептурные лекарственные средства) позволит выявить любые недавние изменения в
                        практике принятия лекарств.
                    </li>
                    <li>
                        Для некоторых резидентов, страдающих болезнью Альцгеймера, некоторые лекарства могут быть
                        почти бесполезны для предотвращения ухудшения состояния когнитивных способностей резидента.
                        Если эти лекарственные средства не принимаются или никогда не принимались, обсудите такой
                        вариант с терапевтом.
                    </li>
                    <li>
                        Внимательно отслеживайте хроническое употребление алкоголя, острые случаи употребления
                        алкоголя, а также употребление «уличных наркотиков».
                    </li>
                    <li>
                        Внимательно отслеживайте принятие резидентом травяных средств или альтернативных
                        лекарственных средств.
                    </li>
                </ul>
