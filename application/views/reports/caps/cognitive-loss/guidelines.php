            <h4 class="h4 text-uppercase">
                Непрерывное участие в каждодневной жизни
            </h4>
            <div class="p-l-30">
                <p class="text-bold">
                    Если ли у резидента возможность для самостоятельной деятельности?
                </p>
                <p>
                    Снижение в одной функциональной области не говорит о необходимости принятия полной
                    ответственности в этой области лицом, осуществляющим уход за резидентом, а равным образом оно
                    не должно рассматриваться как безусловное свидетельство ослабления в других областях. Изучите
                    информацию, полученную в ходе проведения оценки, при рассмотрении следующих вопросов:
                </p>
                <ul>
                    <li>
                        Есть ли факторы, которые указывают на то, что резидента можно шире привлекать к уходу за
                        собой (это, например, ситуации, когда резидент демонстрировал более высокую эффективность
                        самостоятельной деятельности, желал добиться большей независимости; это сохранившаяся
                        способность к обучению, сохранившееся управление собственными телом: туловищем, ногами,
                        руками)?
                    </li>
                    <li>
                        Может ли резидент более активно участвовать в принятии решений о своей повседневной жизни?
                    </li>
                    <li>
                        Сохраняет ли резидент какие-либо когнитивные способности, которые позволяют ему принимать
                        более активное участие в принятии решений?
                    </li>
                    <li>
                        Пассивен ли резидент?
                    </li>
                    <li>
                        Сопротивляется ли резидент оказанию ухода за ним?
                    </li>
                    <li>
                        Разбита ли деятельность резидента на подзадачи, которыми можно управлять?
                    </li>
                </ul>

                <p class="text-bold">
                    В каких пределах и с какой частотой меняются функциональные способности резидента?
                </p>
                <p>
                    Функциональные изменения часто являются первыми признаками ослабления когнитивных функций и
                    указывают на необходимость определения обратимых причин этих изменений. Рассмотрите следующие
                    вопросы:
                </p>
                <ul>
                    <li>
                        В какой степени резидент зависим от помощи при передвижении, одевании или еде?
                    </li>
                    <li>
                        Есть ли у резидента проблемы с поддержанием равновесия при стоянии; есть ли риск падения?
                    </li>
                    <li>
                        Может ли резидент стать более независимым при условии адекватных подсказок или
                        восстановительных программ?
                    </li>
                    <li>
                        Состояние питания и потеря веса влияют на физические функции и могут помешать
                        их восстановлению.
                    </li>
                    <li>
                        Поможет ли направление к нужному врачу предотвратить ослабление функций в будущем?
                    </li>
                </ul>

                <p class="text-bold">
                    В какой степени резидент продолжает заниматься повседневной деятельностью и участвовать в
                    обычной жизни?
                </p>
                <p>
                    Программы, посвященные физическим и социальным аспектам жизни резидента, могут иногда смягчить
                    разрушительные симптомы ослабления когнитивных функций. Рассмотрите следующие вопросы:
                </p>
                <ul>
                    <li>
                        Может ли принести пользу корректировка задач или смена среды, в которой выполняются задачи?
                    </li>
                    <li>
                        Предусмотрена ли программа поддержки занятий в малых группах?
                    </li>
                    <li>
                        Оснащена ли окружающая среда специальными стимулами (например, указателями направлений,
