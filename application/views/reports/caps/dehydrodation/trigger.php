            Во-первых, проявляйте достаточную бдительность и выявляйте потребность в лечении всех пациентов, страдающих
            от соответствующих нарушений. Во-вторых, осуществляйте вмешательство, достаточное для восстановления нужного
            количества жидкости в организме пациента и для исправления связанных с этим клинических проблем.
        </p>

