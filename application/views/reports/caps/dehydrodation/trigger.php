    <div class="p-l-30">
        <p>
            Этот ПКО основан на двух пунктах системы оценки interRAI: обезвоживании и недостаточности жидкости в
            организме. И то, и другое имеет относительно слабую распространенность. Доля пациентов, страдающих от
            обезвоживания, по данным оценок, проведенных с помощью инструментов interRAI, находится в диапазоне от
            менее чем 1% до приблизительно 5%; при этом недостаточность жидкости в организме отмечается у 3 – 10%
            пациентов. Если присутствуют обе проблемы, их клиническая важность, скорее всего, выше. Выделяются два
            уровня вмешательства – высокий и низкий (соответственно при высоком и низком риске – прим.перев.). К группе,
            которой показано вмешательство высокого уровня, относят пациентов, у которых явным образом присутствуют
            одна или несколько причин или осложнений обезвоживания. Клиническое течение этих нарушений требует
            немедленного осмотра терапевта. В отношении пациентов, причисленных к группе низкого риска, хотя и
            необходимо осуществлять клинический надзор, в качестве мер вмешательства зачастую можно ограничиться
            увеличением приема жидкости и внимательным мониторингом.
